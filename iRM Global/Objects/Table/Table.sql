  CREATE OR REPLACE EDITIONABLE TRIGGER "TRG_AFTER_INSERT_OR_UPDATE_O_MST_PERM_GROUP" 
AFTER INSERT OR UPDATE ON MST_PERM_GROUP 
FOR EACH ROW 
DECLARE 
    v_error VARCHAR2(700); 
BEGIN 
    IF INSERTING THEN 
 
        IF :NEW.PERMISSION_IDS IS NOT NULL THEN 
 
           INSERT INTO TRN_PERM_GROUP_AND_PERMISSION_MAP ( 
                PERM_GRP_ID, 
                PERM_ID 
            ) (SELECT :NEW.ID, COLUMN_VALUE FROM TABLE(APEX_STRING.SPLIT(:NEW.PERMISSION_IDS, ','))); 
 
        END IF; 
 
        IF :NEW.PERM_GRP_IDS IS NOT NULL THEN 
 
           INSERT INTO TRN_PERM_GRP_WITH_GRP_MAP ( 
                PNT_GRP_ID, 
                CLD_GRP_ID 
            )   
            (SELECT :NEW.ID, COLUMN_VALUE FROM TABLE(APEX_STRING.SPLIT(:NEW.PERM_GRP_IDS, ','))); 
 
        END IF; 
 
    ELSIF UPDATING THEN 
        -- Check if PERMISSION_IDS changed 
        IF NVL(:OLD.PERMISSION_IDS, 'NULL') != NVL(:NEW.PERMISSION_IDS, 'NULL') THEN 
            -- Deactivate old permissions 
            IF :OLD.PERMISSION_IDS IS NOT NULL THEN 
                UPDATE TRN_PERM_GROUP_AND_PERMISSION_MAP  
                SET IS_ACTIVE = 'N'  
                WHERE PERM_GRP_ID = :OLD.ID; 
            END IF; 
 
            -- Insert or update new permission mappings 
            FOR I IN (SELECT COLUMN_VALUE AS PERM FROM TABLE(APEX_STRING.SPLIT(:NEW.PERMISSION_IDS, ','))) LOOP 
 
                MERGE INTO TRN_PERM_GROUP_AND_PERMISSION_MAP T 
                USING (SELECT :OLD.ID AS PERM_GRP_ID, I.PERM AS PERM_ID FROM DUAL) SRC 
                ON (T.PERM_GRP_ID = SRC.PERM_GRP_ID AND T.PERM_ID = SRC.PERM_ID) 
                WHEN MATCHED THEN 
                    UPDATE SET T.IS_ACTIVE = 'Y' 
                WHEN NOT MATCHED THEN 
                    INSERT (PERM_GRP_ID, PERM_ID)  
                    VALUES (SRC.PERM_GRP_ID, SRC.PERM_ID); 
             
            END LOOP; 
 
        END IF; 
 
 
        IF NVL(:OLD.PERM_GRP_IDS, 'NULL') != NVL(:NEW.PERM_GRP_IDS, 'NULL') THEN 
 
 
            IF :OLD.PERM_GRP_IDS IS NOT NULL THEN 
 
                UPDATE TRN_PERM_GRP_WITH_GRP_MAP  
                SET IS_ACTIVE = 'N'  
                WHERE PNT_GRP_ID = :OLD.ID; 
 
            END IF; 
 
            FOR I IN (SELECT COLUMN_VALUE AS GRP FROM TABLE(APEX_STRING.SPLIT(:NEW.PERM_GRP_IDS, ','))) LOOP 
 
                MERGE INTO TRN_PERM_GRP_WITH_GRP_MAP T 
                USING (SELECT :OLD.ID AS PNT_ID, I.GRP AS GRP_ID FROM DUAL) SRC 
                ON (T.PNT_GRP_ID = SRC.PNT_ID AND T.CLD_GRP_ID = SRC.GRP_ID) 
                WHEN MATCHED THEN 
                    UPDATE SET T.IS_ACTIVE = 'Y' 
                WHEN NOT MATCHED THEN 
                    INSERT (PNT_GRP_ID, CLD_GRP_ID)  
                    VALUES (SRC.PNT_ID, SRC.GRP_ID); 
             
            END LOOP; 
 
 
        END IF; 
 
 
    END IF; 
 
EXCEPTION 
    WHEN OTHERS THEN 
        v_error := SQLERRM; 
