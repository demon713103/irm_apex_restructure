FOR EACH ROW 
DECLARE 
    v_count NUMBER; 
    v_map_count NUMBER; 
    v_error VARCHAR2(4000); 
BEGIN 
 
 
    IF INSERTING THEN 
 
 
        IF :NEW.TAGGED_ERP_ID IS NOT NULL THEN 
 
       
            INSERT INTO MST_APP_ERP_DEFAULT_SCHEMA_MAPPING (APP_ID, ERP_ID, IS_ACTIVE) 
            SELECT :NEW.APP_ID, v_erp.COLUMN_VALUE, 'Y' 
            FROM TABLE(APEX_STRING.SPLIT(:NEW.TAGGED_ERP_ID, ':')) v_erp; 
 
 
 
        ELSE  
 
            INSERT INTO MST_APP_ERP_DEFAULT_SCHEMA_MAPPING (APP_ID, IS_ACTIVE) 
            VALUES (:NEW.APP_ID, 'Y'); 
         
        END IF; 
    
    ELSIF UPDATING THEN 
             
        IF NVL(:NEW.TAGGED_ERP_ID, 0) != NVL(:OLD.TAGGED_ERP_ID, 0) THEN 
 
            UPDATE MST_APP_ERP_DEFAULT_SCHEMA_MAPPING  
            SET IS_ACTIVE = 'N'  
            WHERE APP_ID = :OLD.APP_ID; 
 
        end if; 
 
 
        IF :NEW.TAGGED_ERP_ID IS NULL THEN 
 
            select count(1) into v_map_count from MST_APP_ERP_DEFAULT_SCHEMA_MAPPING where APP_ID = :OLD.APP_ID AND IS_ACTIVE = 'Y' AND ERP_ID IS NULL; 
 
            IF v_map_count = 0 THEN 
             
                INSERT INTO MST_APP_ERP_DEFAULT_SCHEMA_MAPPING (APP_ID, IS_ACTIVE) 
                VALUES (:NEW.APP_ID, 'Y'); 
            END IF; 
 
        ELSE 
 
            IF NVL(:NEW.TAGGED_ERP_ID, 0) != NVL(:OLD.TAGGED_ERP_ID, 0) THEN 
 
 
                
                FOR rec IN ( 
                    SELECT column_value AS erp_id  
                    FROM TABLE(apex_string.split(:NEW.TAGGED_ERP_ID, ':')) 
                ) LOOP 
                    
                    BEGIN 
                     
                        SELECT COUNT(1)  
                        INTO v_count  
                        FROM MST_APP_ERP_DEFAULT_SCHEMA_MAPPING  
                        WHERE APP_ID = :NEW.APP_ID  
                        AND ERP_ID = rec.erp_id; 
 
                    EXCEPTION 
                        WHEN OTHERS THEN 
                            v_error := SQLERRM; 
                            -- INSERT INTO ERROR_LOG (CONTEXT) VALUES ('ERROR : ' || v_error); 
                    END; 
 
                  
                    IF v_count > 0 THEN 
 
                        UPDATE MST_APP_ERP_DEFAULT_SCHEMA_MAPPING  
                        SET IS_ACTIVE = 'Y'  
                        WHERE APP_ID = :NEW.APP_ID  
                        AND ERP_ID = rec.erp_id; 
 
                    ELSE 
                      
                        INSERT INTO MST_APP_ERP_DEFAULT_SCHEMA_MAPPING (APP_ID, ERP_ID, IS_ACTIVE) 
                        VALUES (:NEW.APP_ID, TO_NUMBER(rec.erp_id), 'Y'); 
 
                    END IF; 
 
                END LOOP; 
            END IF; 
         
        END IF; 
    END IF; 
END;
/
