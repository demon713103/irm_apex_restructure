  CREATE OR REPLACE EDITIONABLE TRIGGER "TRG_AFTER_INSERT_OR_UPDATE_ON_TRN_USER_ROLE_MAP" 
after insert or update on "TRN_USER_ROLE_MAP" 
for each row 
DECLARE 
    V_ERROR VARCHAR2(4000); 
begin 
 
 
    IF INSERTING THEN 
 
 
        INSERT INTO TRN_USER_MULTI_ROLE_MAP (USER_ID, ROLE_ID)
        SELECT :NEW.USER_ID, column_value
        FROM TABLE(APEX_STRING.SPLIT(:NEW.ROLE_ID, ','));

    ELSE 
 
        IF NVL(:NEW.ROLE_ID, 'NULL') != NVL(:OLD.ROLE_ID, 'NULL') THEN 
 
            IF NVL(:OLD.ROLE_ID, 'NULL') IS NOT NULL THEN 
 
                UPDATE TRN_USER_MULTI_ROLE_MAP  
                SET  
                    IS_ACTIVE = 'N'  
                WHERE USER_ID = :OLD.USER_ID; 
 
            END IF; 
 
 
            FOR I IN (SELECT COLUMN_VALUE AS ROL FROM TABLE(APEX_STRING.SPLIT(:NEW.ROLE_ID, ','))) LOOP 
 
                MERGE INTO TRN_USER_MULTI_ROLE_MAP T 
                USING (SELECT :OLD.USER_ID AS V_USER, I.ROL AS V_ROLE FROM DUAL) SRC 
                ON (T.USER_ID = SRC.V_USER AND T.ROLE_ID = SRC.V_ROLE) 
                WHEN MATCHED THEN 
                    UPDATE SET T.IS_ACTIVE = 'Y' 
                WHEN NOT MATCHED THEN 
                    INSERT (USER_ID, ROLE_ID)  
                    VALUES (SRC.V_USER, SRC.V_ROLE); 
             
            END LOOP; 
 
 
        END IF; 
 
    END IF; 
EXCEPTION WHEN OTHERS THEN 
    V_ERROR := SQLERRM; 
    DBMS_OUTPUT.PUT_LINE('ERROR : ' || V_ERROR); 
 
end;
/
ALTER TRIGGER "TRG_AFTER_INSERT_OR_UPDATE_ON_TRN_USER_ROLE_MAP" ENABLE; 